#adalah operasi yang dapat dilakukan dengan penyingkatan
# operasi ditambah asignment
a = 5 #assignment
print("Nilai a adalah", a)

a += 1 #penambahan
print("Nilai a adalah", a)

a -= 2 #pengurangan
print("Nilai a adalah", a)

a *= 5 #perkalian
print("Nilai a adalah", a)

a /= 2 #pembagian
print("Nilai a adalah", a)

b = 10
print("Nilai b adalah", b)

b %= 3 #modulus
print("Nilai b adalah", b)

b = 10
b //= 3 #floor division
print("Nilai b adalah", b)

a = 5
a **= 3 #pangkat
print("Nilai a adalah", a)

#operasi bitwise
c = True
c |= False #OR
print("Nilai c adalah", c)

c = True
c &= False #AND
print("Nilai c adalah", c)

c = True
c ^= True #XOR
print("Nilai c adalah", c)

#geser geser
d = 0b0100
print("Nilai d adalah", format(d, '04b'))
d >>= 2 #geser ke kanan
print("Nilai d adalah", format(d, '04b'))
d <<= 1 #geser ke kiri
print("Nilai d adalah", format(d, '04b'))




